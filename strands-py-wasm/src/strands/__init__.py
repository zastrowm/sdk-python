class Message(_t.Message):
    def __init__(
        self,
        *,
        role: _t.Role,
        content: Iterable[Any],
        metadata: _t.MessageMetadata | None = None,
    ) -> None:
        super().__init__(
            role=role,
            content=[_as_content_block(c) for c in content],
            metadata=metadata,
        )

    @classmethod
    def user(cls, *content: Any, metadata: _t.MessageMetadata | None = None) -> Message:
        return cls(role=_t.Role.USER, content=content, metadata=metadata)

    @classmethod
    def assistant(cls, *content: Any, metadata: _t.MessageMetadata | None = None) -> Message:
        return cls(role=_t.Role.ASSISTANT, content=content, metadata=metadata)


