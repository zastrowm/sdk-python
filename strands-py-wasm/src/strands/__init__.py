"""Strands Agents SDK Python surface.

Wire types live in :mod:`strands._generated`, auto-generated by ``bindgen``.
Those classes are accepted directly by wasmtime-py at the FFI boundary:
kebab-case record attributes, ``Variant(tag, payload)`` for tagged variants,
raw payloads for untagged ones.

This module wraps the wire types with ergonomic Python helpers: builders
that take snake_case kwargs, factories that fill variant arms, and SDK-level
event classes that lift wire-shape ``StreamEvent`` values into a typed Python
class hierarchy so users can dispatch with ``isinstance``.
"""

